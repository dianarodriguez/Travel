    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int cont =0;
         UsuarioJpaController ujpa= new UsuarioJpaController();
        String  array="";
       for (int i = 0; i < ujpa.findUsuarioEntities().size(); i++) {
            Usuario object = ujpa.findUsuarioEntities().get(i);
           if(object.getCi().equals(jTextField1.getText())){
                array=object.getRol().getNombrerol();
                cont++;
           }

        }
        if(cont==0)
               JOptionPane.showMessageDialog(this, "No existe usuario con ese CI");
        jTextField2.setText(array);
    }//GEN-LAST:event_jButton1ActionPerformed

