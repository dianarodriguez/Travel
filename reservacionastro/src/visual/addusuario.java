    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            int b=0;
            int n=0;
            int idrol=0;
            Usuario u = new Usuario();
            Rol rol = new Rol();
            rolcontrol  rolc= new rolcontrol();
            usuariocontrol uc = new usuariocontrol();
            for (int i = 0; i < uc.listar1().size(); i++) {
                Usuario object = uc.listar1().get(i);
                if (object.getCi().equals(jTextField3.getText())){
                    b++;
                     JOptionPane.showMessageDialog(this, "Este usuario ya existe");break;
                 }
                     if (object.getNombreusuario().equals(jTextField6.getText())){
                    n++;
                    JOptionPane.showMessageDialog(this, "Este nombre de usuario ya existe");break;
                }
               }
                        if (b==0 && n==0) {

                            for (int j = 0; j < rolc.listar1().size(); j++) {
                                Rol object = rolc.listar1().get(j);
                                if (object.getNombrerol().equals(jComboBox1.getSelectedItem())){
                                     idrol=object.getId();
                                     rol=object;
                                }

                            }
                               if (idrol!=0)  {
                                   u.setNombre(jTextField1.getText());
                           u.setApellido(jTextField2.getText());
                       u.setCi(jTextField3.getText());
                       u.setDireccion(jTextField4.getText());
                       u.setNombreusuario(jTextField6.getText());
                      u.setLogueado("no");
                      u.setOcupacion(jTextField5.getText());
                      u.setCorreo(jTextField7.getText());
                      u.setRol(rol);
                     uc.adicionar(u);
                      JOptionPane.showMessageDialog(this, "El usuario se ha adicionado");
                               }
 else
      JOptionPane.showMessageDialog(this, "Este nombre de usuario ya existe");
                        }

        } catch (PreexistingEntityException ex) {
            Logger.getLogger(addusuario.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(addusuario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

