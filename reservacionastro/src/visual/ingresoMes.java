        try {
            // TODO add your handling code here:
            reservacioncontrol rc = new reservacioncontrol();
            int mes=0;
            for (int i = 0; i < rc.listar1().size(); i++) {
                Reservacion object = rc.listar1().get(i);
                if (object.getMessalida().equals(String.valueOf(jComboBox1.getSelectedItem()))){
                    mes++;
                }
            }
            if(mes==0){
                JOptionPane.showMessageDialog(this, "Este mes no reportÃ³ ingresos");
            }
            else{
            double ing = 0;
            ing = rc.ingresosMes(String.valueOf(jComboBox1.getSelectedItem()),String.valueOf(jComboBox2.getSelectedItem()));
            jTextField2.setText(String.valueOf(ing));
             }
        } catch (PreexistingEntityException ex) {
            Logger.getLogger(ingresoMes.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(ingresoMes.class.getName()).log(Level.SEVERE, null, ex);
        }
