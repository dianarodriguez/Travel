        int temp =0;
         int adm=0;
         int bandera = 0;
         int listae =0;
        while ( temp==0){
         Passwords passw = new Passwords();


        PasswordsJpaController pj= new PasswordsJpaController();
        String pass="";
        String corredor="";

        for (int i = 0; i < jPasswordField1.getPassword().length; i++) {
            corredor = String.valueOf(jPasswordField1.getPassword()[i]);
           pass+=corredor;

        }

        if (jTextField1.getText().equals("administrador")&& pass.equals("adminpass+2012")){
            Administrador admin = new Administrador(null,true);
        admin.setVisible(true);
        setVisible(false);
        }
 else{
            int nu=0;
            int c=0;
        for (int j = 0; j < pj.findPasswordsEntities().size(); j++) {
          Passwords  pw = pj.findPasswordsEntities().get(j);
          if (jTextField1.getText().equals(pw.getNombreusuario()))
                        nu++;
          if(pass.equals(pw.getPassword()))
              c++;
          }
            if(nu==0){
                 JOptionPane.showMessageDialog(this, "El nombre de usuario no existe");break;
            }
                
            if((nu==1 )&& (c==0)){
                 JOptionPane.showMessageDialog(this, "ContraseÃ±a incorrecta");break;
            }
                
            if ((nu==1)&& (c==1)){
                temp=1;
                try {

                    Usuario u = new Usuario();
                    usuariocontrol uc = new usuariocontrol();
                    for (int k = 0; k < uc.listar1().size(); k++) {
                        u = uc.listar1().get(k);
                        if ((jTextField1.getText().equals(u.getNombreusuario()) && (u.getRol().getId()==1))) {
                            bandera = 1;
                        }
                        if((jTextField1.getText().equals(u.getNombreusuario()) && (u.getRol().getId()==3))) {
                            adm = 1;
                        }
                        if((jTextField1.getText().equals(u.getNombreusuario()) && (u.getRol().getId()==2))) {
                            listae = 1;
                        }

                    }

                } catch (PreexistingEntityException ex) {
                    Logger.getLogger(entrada.class.getName()).log(Level.SEVERE, null, ex);
                } catch (Exception ex) {
                    Logger.getLogger(entrada.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            if (bandera==1){
                    principal pal = new principal();
                    pal.setVisible(true);
                 setVisible(false);
                }
            if (adm==1){
                Administrador admin = new Administrador(null,true);
        admin.setVisible(true);
        setVisible(false);
            }


             if (listae==1){
              /* ListadeEspera l= new ListadeEspera();
        l.setVisible(true);
        setVisible(false);*/
RecepcionistaLE r = new RecepcionistaLE();
r.setVisible(true);
setVisible(false);
            }


 }}
        if((bandera==0)&&( adm==0)&&(listae==0) )
                JOptionPane.showMessageDialog(this, "No tiene permisos para entrar al sitema");
