    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            int existe=0;
            Rol rol = new Rol();
            rolcontrol rc = new rolcontrol();
            for (int i = 0; i < rc.listar1().size(); i++) {
                Rol object = rc.listar1().get(i);
                if (object.getNombrerol().equals(jComboBox1.getSelectedItem())){
                    existe=1;
                    JOptionPane.showMessageDialog(this, "El rol ya existe");
                }
                }
                if(existe==0){
                    rol.setNombrerol(String.valueOf(jComboBox1.getSelectedItem()));
               rc.adicionar(rol);
                JOptionPane.showMessageDialog(this, "El rol fue adicionado");
                }
            
        } catch (PreexistingEntityException ex) {
            Logger.getLogger(addrol.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(addrol.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

