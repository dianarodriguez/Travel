    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            listaesperacontrol le = new listaesperacontrol();
            Listaespera L = new Listaespera();
            int cont=0;
            for (int i = 0; i < le.listaespera().size(); i++) {
                Listaespera object = le.listaespera().get(i);
                if(object.getRuta().equals(String.valueOf(jComboBox2.getSelectedItem())))
                    cont++;
            }

            jTextField1.setText(String.valueOf(cont));
            if (cont==0)
                 JOptionPane.showMessageDialog(this, "No hay clientes anotados para este destino");
        } catch (PreexistingEntityException ex) {
            Logger.getLogger(NroLEDestino.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(NroLEDestino.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

